#Напишете програма, която чете от конзолата три числа b1, b2 и h
# и пресмята лицето на трапец с основи b1 и b2 и височина h.
# Формулата за лице на трапец е (b1 + b2) * h / 2.
#прочитаме данните от конзилата като реални числа
# пресмятаме данните по формулата
#принтираме резултатът със закръгляне до втория знак след запетаята
